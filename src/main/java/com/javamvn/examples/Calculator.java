package com.javamvn.examples;

public class Calculator {
    
    /** {@inheritDoc} */
    public final int add(final int a, final int b) {
        return a + b;
    }
    public int subtract(final int a, final int b) {
        return a - b;
    }

    public int multiplication(final int a, final int b) {
        return a * b;
    }
}
